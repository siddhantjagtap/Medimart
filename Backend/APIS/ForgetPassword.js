const express = require('express');
const app = express();
const ResetPasswordSchema = require('../models/user.model');
const nodemailer = require('nodemailer');
const crypto = require('crypto');
const cors = require('cors');
const bodyParser = require('body-parser');

app.use(cors());
app.use(bodyParser.json());

const FRONTEND_URL = process.env.FRONTEND_URL;
const EMAIL = process.env.EMAIL;
const PASS = process.env.PASS;
const forgotPassword = async (req, res) => {
    const { email } = req.body;
    try {
        const user = await ResetPasswordSchema.findOne({ email });
        if (!user) {
            return res.json({ message: 'User not found' });
        }
        const token = crypto.randomBytes(20).toString('hex');
        user.resetPasswordToken = token;
        user.resetPasswordExpires = Date.now() + 3600000;
        await user.save();

        const transporter = nodemailer.createTransport({
            service: 'Gmail',
            auth: {
                user: `${EMAIL}`,
                pass: `${PASS}`
            }
        });
        const mailOptions = {
            from: 'thewitcher1501@gmail.com',
            to: email,
            subject: 'Reset your password',
            text: `You are receiving this because you (or someone else) have requested to reset the password for your account.\n\n` +
                `Please click on the following link, or paste this into your browser to complete the process:\n\n` +
                `${FRONTEND_URL}reset-password/${token}\n\n` +
                `If you did not request this, please ignore this email and your password will remain unchanged.\n`
        };

        transporter.sendMail(mailOptions, (error, info) => {
            if (error) {
                console.log(error);
                return res.status(500).send({ message: 'Error in sending email' });
            }
           // console.log('Email sent: ' + info.response);
            res.json({ message: 'Email sent successfully' });
        });

    } catch (error) {
        console.log(error);
        res.status(500).send({ message: 'Internal Server Error' });
    }
};

module.exports = {forgotPassword}
